package com.Proyecto.demo.Entity;

public class Tarjetas {

    private Long id;
    private String numero;

    public Tarjetas() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNumero() {
        return numero;
    }

    public void setNumero(String numero) {
        this.numero = numero;
    }

}
